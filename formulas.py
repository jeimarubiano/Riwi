# ==========================================
# FIGURAS 2D
# ==========================================

# CUADRADO
# dato: lado
# Area = lado ** 2
# Perimetro = lado * 4

# RECTANGULO
# datos: base, altura
# Area = base * altura
# Perimetro = (base + altura) * 2

# CIRCULO
# dato: radio
# Area = pi * radio ** 2
# Perimetro = 2 * pi * radio

# ROMBO
# datos: diagonal1, diagonal2, lado
# Area = (diagonal1 * diagonal2) / 2
# Perimetro = lado * 4

# TRIANGULO RECTANGULO
# datos: base, altura
# Area = (base * altura) / 2
# Hipotenusa = sqrt(base ** 2 + altura ** 2)
# Perimetro = base + altura + hipotenusa

# ==========================================
# FIGURAS 3D
# ==========================================

# CUBO
# dato: lado
# Volumen = lado ** 3
# Superficie = lado ** 2 * 6

# ESFERA
# dato: radio
# Volumen = (4/3) * pi * radio ** 3
# Superficie = 4 * pi * radio ** 2

# CILINDRO
# datos: radio, altura
# Volumen = pi * radio ** 2 * altura
# Superficie = (2 * pi * radio * altura) + (2 * pi * radio ** 2)

# CONO
# datos: radio, altura
# Generatriz = sqrt(radio ** 2 + altura ** 2)
# Volumen = (1/3) * pi * radio ** 2 * altura
# Superficie = pi * radio * (radio + generatriz)
